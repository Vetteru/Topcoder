  int V;
  vector<int> G[MAX_V];
  int match[MAX_V];
  bool used[MAX_V];

  int getNumHats(vector <int> topHeight, vector <int> topRadius,
		 vector <int> bottomHeight, vector <int> bottomRadius) {

    int N = topHeight.size();
    int M = bottomHeight.size();
    V = N+M+2;
    for(int i = 0; i < MAX_V; i++) G[i].clear();

    vector<Hat> T(N), B(M);
    for(int i = 0; i < N; i++) T[i] = (Hat){topHeight[i], topRadius[i]};
    for(int i = 0; i < M; i++) B[i] = (Hat){bottomHeight[i], bottomRadius[i]};
    
    for(int i = 0; i < N; i++)
      for(int j = 0; j < M; j++)
	if(T[i].h*B[j].r > T[i].r*B[j].h && T[i].r < B[j].r) add_edge(i,N+1+j);
    return bipartite_matching();
  }


  void add_edge(int u, int v){
    G[u].push_back(v);
    G[v].push_back(u);
  }
  
  bool dfs(int v){
    used[v] = true;
    for(int i = 0; i < (int)G[v].size(); i++){
      int u = G[v][i];
      int w = match[u];
      
      if(w < 0 || (!used[w] && dfs(w))){
	match[v] = u;
	match[u] = v;
	return true;
      }
    }
    return false;
  }
  
  int bipartite_matching(){
    int res = 0;
    memset(match, -1, sizeof(match));
    for(int v = 0; v < V; v++){
      if(match[v] < 0){
	memset(used,0,sizeof(used));
	if(dfs(v)) res++;
      }
    }
    return res;
  }

	
